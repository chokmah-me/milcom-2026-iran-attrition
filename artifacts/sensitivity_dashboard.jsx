function parse(csv) {
  const ls = csv.trim().split("\n");
  const hs = ls[0].split(",");
  return ls.slice(1).map(l => {
    const vs = l.split(",");
    const o = {};
    hs.forEach((h, i) => o[h] = isNaN(vs[i]) ? vs[i] : parseFloat(vs[i]));
    return o;
  });
}

